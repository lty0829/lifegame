	 private int rows = world.length;
	 private int cols = world[0].length;
	 public int[][] nextmap = new int[rows][cols];
	 public int[][] nextnextmap = new int[rows][cols];
	 private int leng = 13;
	 private Timer timer;
	 
	 public World() {  
		 for (int i = 0; i < rows; i++) {  
	            for (int j = 0; j < cols; j++) {  
	                nextmap[i][j] = world[i][j];  
	                nextnextmap[i][j] = world[i][j];  
	            }  
	        }  
	        this.startAnimation();  
	        
	 }  
	 public void startAnimation(){
		 timer = new Timer(1200, new ActionListener() {
			
			@Override
			public void actionPerformed(ActionEvent e) {
				// TODO Auto-generated method stub
				changemap(nextmap,nextnextmap);
				repaint();
			}
		});
		timer.start(); 
	 }
	 
	 @Override
	 protected void paintComponent(Graphics g) {
		super.paintComponent(g);  
		for(int i=0;i<rows;i++){
			for(int j=0;j<cols;j++){
				if(nextmap[i][j]==0){
					g.drawRect( j * leng, i * leng, leng, leng); 
				}
				else{
					g.fillRect( j * leng, i * leng, leng, leng); 
				}
			}
		 }
	 }
	 public void changemap(int first[][],int second[][]){
		 for(int i=0;i<first.length;i++){
			 for(int j=0;j<first[0].length;j++){
				 int num = getnext(i, j,second);
				 if(num==0||num==1||num==4||num==5||num==6||num==7
						 ||num==8){
					 first[i][j] = 0;
				 }
				 else if(num==2){
					 first[i][j] = second[i][j];
				 }
				 else if(num==3){
					 first[i][j] = 1;
				 }
			 }
		 }
		 
		 for(int i=0;i<first.length;i++){
			 for(int j=0;j<first[0].length;j++){
				 second[i][j] = first[i][j];
			 }
		 }
	 }
	 
	 
	 
	 public int getnext(int row,int col,int second[][]){
		 int num = 0;
		 for(int i=row-1;i<=row+1;i++){
			 for(int j=col-1;j<=col+1;j++){
				 if (i < 0 || i >= second.length ||j < 0  
	                        || j >= second[0].length||(i==row&&j==col)) {  
	                    continue;  
	                }  
	                if (second[i][j] == 1) {  
	                    num++;            
	                }  
			 }
		 }
		
		 return num;
	 }
	

